label START:
IMM 0 ADDR
IMM 0b00010000 DATA
IMM 0b00110000 DATA
IMM 0b00100000 DATA
IMM 0b00100001 DATA
IMM 0b00000001 DATA
# Draw the line across the top
IMM 7 0 # Set the upper limit of the for loop
label NOT_DONE:
INC ADDR
IMM 0b00000001 DATA
DEC ADDR
IMM 0b00000000 DATA
INC ADDR
NEQ ADDR 0 NOT_DONE
IMM 0b00000011 DATA
IMM 0b00000010 DATA
IMM 0b00000110 DATA
IMM 0b00000100 DATA
IMM 0b00001100 DATA
IMM 0b00001000 DATA
IMM 0 0 # Set the lower limit of the for loop
label NOT_DONE_2:
DEC ADDR
IMM 0b00001000 DATA
INC ADDR
IMM 0b00000000 DATA
DEC ADDR
NEQ ADDR 0 NOT_DONE_2
JMP START
HLT
